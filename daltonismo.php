<h4>
Existe cura para o daltonismo?
</h4>

<br>

<p>
Uma equipe formada por cientistas da Universidade de Khalifa, de Abu Dhabi, criou lentes de contato com ouro que são capazes de corrigir o daltonismo de quem não consegue distinguir a cor verde do vermelho. Os pesquisadores misturaram nanopartículas do metal em um hidrogel pra desenvolver um protótipo das lentes de contato. Essa mistura deu origem a um gel vermelho que filtra os comprimentos de onda de luz onde o verde e o vermelho se sobrepõem, o que causa as dificuldades dos daltônicos de distinguir as cores. De acordo com o estudo, foram utilizadas partículas de ouro muito finas, com apenas 40 nanômetros de largura. Para se ter ideia, uma folha de papel tem cerca de 100 nanômetros de largura. Esse material utilizado é mais seguro para os pacientes do que o que foi testado em outras lentes de contato anteriormente. Em outras pesquisas, para tentar corrigir o daltonismo, foram desenvolvidos produtos com o uso de tinta de coloração rosa choque, o que causou preocupações relacionadas à durabilidade e segurança para os usuários.
Além destas lentes, também foi desenvolvido um óculos que que da a sensação de correção da visão, as lentes do acessório têm filtros capazes de aumentar o contraste entre os sinais das cores verde e vermelho. "Não é uma cura para o daltonismo. O que os óculos fazem é modificar a forma como as pessoas percebem a luz", explica o oftalmologista Pedro Carlos Carricondo, do Conselho Brasileiro de Oftalmologia e diretor do pronto-socorro do Hospital de Oftalmologia do Hospital das Clínicas, em São Paulo.
</p>

