<h4>
Daltonismo
</h4>

<br>

<p>
O daltonismo é a incapacidade de distinguir entre tons de vermelho e verde. Não tão comum, portadores dessa doença também enfrentam dificuldades com o azul e o amarelo e, mais raramente, enxergam apenas em tons de preto, branco e cinza. As classificações de daltonismo são: Acromático, Protanopia, Deuteranopia e Tritanopia.
</p>

